package Kontrollstrukturen.Iterationen.ForSchleife.Aufgabe1;

public class SwitchStruktur {

	public static void main(String[] args) {
		for (int j = 1; j < 2; j++) {
			switch (j) {
			case 1:
				System.out.println("1");
			case 2:
				System.out.println("2");
			default:
				System.out.println("Standard");
			}
		}
	}

}
