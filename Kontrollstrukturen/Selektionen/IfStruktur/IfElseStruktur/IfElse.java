package Kontrollstrukturen.Selektionen.IfStruktur.IfElseStruktur;

public class IfElse {

	public static void main(String[] args) {
		String anrede = "Mann";
		if (anrede == "Frau") {
			System.out.println("Ich bin eine Frau!");
		} else {
			System.out.println("Ich bin ein Mann!");
		}
	}

}
