package Kontrollstrukturen.Selektionen.IfStruktur.ElseIfStruktur;

public class IfElse {

	public static void main(String[] args) {
		String anrede = "Kind";
		
		if (anrede == "Frau") {
			System.out.println("Ich bin eine Frau!");
		} else if (anrede == "Mann") {
			System.out.println("Ich bin ein Mann!");
		} else if (anrede == "Kind") {
			System.out.println("Ich bin ein Kind!");
		}
	}

}
